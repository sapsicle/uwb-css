public class Time {
    private short hour, minute, second;

    public short getHour() {
        return this.hour;
    }

    public void setHour(short hour) {
        this.hour = hour;
    }

    public short getMinute() {
        return this.minute;
    }

    public void setMinute(short minute) {
        this.minute = minute;
    }

    public short getSecond() {
        return this.second;
    }

    public void setSecond(short second) {
        this.second = second;
    }

}
